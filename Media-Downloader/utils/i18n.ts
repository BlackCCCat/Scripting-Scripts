export function localizeRuntimeText(text: string, mode: LanguageMode): string {
  if (resolveLanguage(mode) === "zh") return text
  const timestampMatch = text.match(/^(\[\d{2}:\d{2}:\d{2}\]\s*)(.*)$/)
  const prefix = timestampMatch?.[1] || ""
  let next = timestampMatch?.[2] || text
  for (const [pattern, replacement] of RUNTIME_EN_REPLACEMENTS) {
    next = next.replace(pattern, replacement)
  }
  for (const [pattern, replacement] of RUNTIME_EN_TOKEN_REPLACEMENTS) {
    next = next.replace(pattern, replacement)
  }
  return `${prefix}${next}`
}
